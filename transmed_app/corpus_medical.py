# =====================================================================
# 9. 综合专业医学语料库 (Unified Professional Medical Corpus)
#
# 把 ICD-10 疾病、症状学、解剖、检验、药品、科室、影像、缩写
# 统一合并为 MEDICAL_CORPUS，供 RAG 检索使用。
#
# 同时提供 build_professional_rag_docs() 生成适合 translator 用的
# RAG 文档结构（content/meta/keywords）。
# =====================================================================
from typing import List as _List, Dict as _Dict, Tuple as _Tuple

# 合并为 MEDICAL_CORPUS
# key: 标准化英文 term（小写去空格）
# value: {"en": 英文, "zh": 中文, "latin": 拉丁文/缩写, "category": 分类, "extra": 附加信息}
MEDICAL_CORPUS: _Dict[str, _Dict[str, str]] = {}


def _add_corpus(term_en: str, term_zh: str, category: str, latin: str = "", extra: str = ""):
    key = (term_en or "").strip().lower()
    if not key:
        return
    if key in MEDICAL_CORPUS:
        # 保留更丰富的一条
        existing = MEDICAL_CORPUS[key]
        if not existing.get("zh"):
            existing["zh"] = term_zh
        if latin and not existing.get("latin"):
            existing["latin"] = latin
        if extra and not existing.get("extra"):
            existing["extra"] = extra
        if category and not existing.get("category"):
            existing["category"] = category
    else:
        MEDICAL_CORPUS[key] = {
            "en": term_en,
            "zh": term_zh,
            "latin": latin,
            "category": category,
            "extra": extra,
        }


def build_medical_corpus_index() -> _Dict[str, _Dict[str, str]]:
    """从上面所有列表构建 MEDICAL_CORPUS 索引，并返回引用。"""
    if MEDICAL_CORPUS:
        return MEDICAL_CORPUS

    # 1. ICD-10 疾病
    for (en, zh, icd, cat) in ICD10_DISEASES:
        _add_corpus(en, zh, f"disease/{cat}", latin=icd, extra=f"ICD-10 {icd}")

    # 2. 症状学
    for (en, zh, cat) in SYMPTOMS:
        _add_corpus(en, zh, f"symptom/{cat}" if cat else "symptom")

    # 3. 解剖学
    for item in ANATOMY_TERMS:
        if len(item) >= 2:
            en, zh = item[0], item[1]
            _add_corpus(en, zh, "anatomy")

    # 4. 检验医学
    for item in LAB_TESTS:
        if len(item) >= 2:
            en, zh = item[0], item[1]
            abbr = item[2] if len(item) >= 3 else ""
            _add_corpus(en, zh, "lab", latin=abbr, extra=abbr)

    # 5. 常用药品
    for item in MEDICATIONS_EX:
        if len(item) >= 2:
            en, zh = item[0], item[1]
            cat = item[2] if len(item) >= 3 else ""
            dose = item[3] if len(item) >= 4 else ""
            _add_corpus(en, zh, f"medication/{cat}", latin=cat, extra=dose)

    # 6. 医院科室
    for item in DEPARTMENTS:
        if len(item) >= 2:
            en, zh = item[0], item[1]
            abbr = item[2] if len(item) >= 3 else ""
            _add_corpus(en, zh, "department", latin=abbr, extra=abbr)

    # 7. 影像学
    for item in IMAGING_TERMS:
        if len(item) >= 2:
            en, zh = item[0], item[1]
            abbr = item[2] if len(item) >= 3 else ""
            _add_corpus(en, zh, "imaging", latin=abbr, extra=abbr)

    # 8. 常用缩写
    for item in MEDICAL_ABBREVIATIONS:
        if len(item) >= 2:
            en, zh = item[0], item[1]
            _add_corpus(en, zh, "abbreviation")

    return MEDICAL_CORPUS


def build_professional_rag_docs() -> _List[_Dict]:
    """构建专业医学 RAG 文档集合。返回结构化 docs 列表。"""
    corpus = build_medical_corpus_index()
    docs: _List[_Dict] = []

    # 先把每个术语压成一条文档
    for key, info in corpus.items():
        en = info.get("en", "")
        zh = info.get("zh", "")
        latin = info.get("latin", "")
        category = info.get("category", "")
        extra = info.get("extra", "")

        parts = [f"{en} → {zh}"]
        if latin:
            parts.append(f"（缩写/编码：{latin}）")
        if extra:
            parts.append(f"[{extra}]")
        content = " ".join(p for p in parts if p)

        meta = category or "medical"
        keywords = [k for k in [en, zh, latin, category] if k]
        priority = 2 if "medication" in category or "disease" in category else 1

        docs.append({
            "content": content,
            "meta": meta,
            "keywords": keywords,
            "priority": priority,
        })

    return docs


# 启动时构建一次，便于外部直接使用
MEDICAL_CORPUS_DOCS: _List[_Dict] = []


def _bootstrap_corpus():
    build_medical_corpus_index()
    global MEDICAL_CORPUS_DOCS
    MEDICAL_CORPUS_DOCS = build_professional_rag_docs()


_bootstrap_corpus()

